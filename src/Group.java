
public class Group {
	private String groupName , groupCreator;
	private Boolean isPrivateGroup;
	public String getGroupCreator() {
		return groupCreator;
	}
	public void setGroupCreator(String groupCreator) {
		this.groupCreator = groupCreator;
	}
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}
	public Boolean getIsPrivateGroup() {
		return isPrivateGroup;
	}
	public void setIsPrivateGroup(Boolean isPrivateGroup) {
		this.isPrivateGroup = isPrivateGroup;
	}
}
